#include <iostream>

int main() {
  // Випадкове число від 1 до 100
  int number = rand() % 100 + 1;

  // Вивід випадкового числа на консоль
  std::cout << "Випадкове число: " << number << std::endl;

  // Якщо число парне, виведіть "Число парне"
  if (number % 2 == 0) {
    std::cout << "Число парне" << std::endl;
  }

  // Якщо число непарне, виведіть "Число непарне"
  else {
    std::cout << "Число непарне" << std::endl;
  }

  return 0;
}
